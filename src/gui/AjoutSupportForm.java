    public void load() {
        model.setRowCount(0);
        for (AjoutSupport as : ass.findAll()) {
            model.addRow(new Object[]{
                as.getSupport().getTitre(),
                as.getProfesseur().getNom(),
                as.getDateAjout()
            });
        }
    }

    public void loadProfesseurs() {
        listProfesseurs.addItem("Choisir un professeur");
        for (Professeur f : ps.findAll()) {
            listProfesseurs.addItem(f);
        }
    }

    public void loadSupports() {
        listSupports.addItem("Choisir un support");
        for (Support s : ss.findAll()) {
            listSupports.addItem(s);
        }
    }

